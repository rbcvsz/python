'''
função vogal que recebe um único caractere como parâmetro
e devolve True se ele for uma vogal e False se for uma consoante.
'''

def vogal(x):
    if x == 'a' or x == 'A':
        return True
    if x == 'e' or x == 'E':
        return True
    if x == 'i' or x == 'I':
        return True
    if x == 'o' or x == 'O':
        return True
    if x == 'u' or x == 'U':
        return True
    else:
        return False



    
